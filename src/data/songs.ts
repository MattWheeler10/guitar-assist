export function getSongs(): Song[] {
  const stored = localStorage.getItem('guitar-assist-songs')
  if (stored) {
    return JSON.parse(stored)
  }
  localStorage.setItem('guitar-assist-songs', JSON.stringify(songs))
  return songs
}

export function getSongById(id: string): Song | undefined {
  return getSongs().find(s => s.id === id)
}

export function getSongsByStatus(status: SongStatus): Song[] {
  return getSongs().filter(s => s.status === status)
}
